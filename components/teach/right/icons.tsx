/** Per-resource-kind glyph for the card thumbnail / list chip. */
export function ResourceKindIcon({
  kind,
  size = 22,
}: GlyphProps & { kind: string }): ReactNode {
  const common = {
    width: size,
    height: size,
    viewBox: "0 0 24 24",
    fill: "none",
    stroke: "currentColor",
    strokeWidth: 1.7,
    strokeLinecap: "round" as const,
    strokeLinejoin: "round" as const,
    "aria-hidden": true,
  };
  switch (kind) {
    case "slides":
      return (
        <svg {...common}>
          <rect x="2" y="3" width="20" height="14" rx="2" />
          <line x1="8" y1="21" x2="16" y2="21" />
          <line x1="12" y1="17" x2="12" y2="21" />
        </svg>
      );
    case "pdf":
      return (
        <svg {...common}>
          <path d="M14 2H6a2 2 0 0 0-2 2v16a2 2 0 0 0 2 2h12a2 2 0 0 0 2-2V8z" />
          <polyline points="14 2 14 8 20 8" />
          <line x1="9" y1="15" x2="15" y2="15" />
        </svg>
      );
    case "doc":
      return (
        <svg {...common}>
          <path d="M14 2H6a2 2 0 0 0-2 2v16a2 2 0 0 0 2 2h12a2 2 0 0 0 2-2V8z" />
          <polyline points="14 2 14 8 20 8" />
          <line x1="9" y1="13" x2="15" y2="13" />
          <line x1="9" y1="17" x2="15" y2="17" />
        </svg>
      );
    case "image":
      return (
        <svg {...common}>
          <rect x="3" y="3" width="18" height="18" rx="2" />
          <circle cx="8.5" cy="8.5" r="1.5" />
          <polyline points="21 15 16 10 5 21" />
        </svg>
      );
    case "video":
      return (
        <svg {...common}>
          <polygon points="23 7 16 12 23 17 23 7" />
          <rect x="1" y="5" width="15" height="14" rx="2" />
        </svg>
      );
    case "tool":
      return (
        <svg {...common}>
          <path d="M14.7 6.3a4 4 0 0 0-5.6 5.6l-6 6 2 2 6-6a4 4 0 0 0 5.6-5.6l-2.5 2.5-2-2 2.5-2.5z" />
        </svg>
      );
    case "board":
      // A learning board surfaced as a resource (Wave 4 #9) — mirrors the
      // nav BoardsIcon: a canvas frame with a tile + a couple of content lines.
      return (
        <svg {...common}>
          <rect x="3" y="4" width="18" height="16" rx="2" />
          <rect x="6" y="7" width="5" height="5" rx="1" />
          <path d="M14 7.5h4M14 11h4M6 15.5h12" />
        </svg>
      );
    case "link":
    default:
      return (
        <svg {...common}>
          <circle cx="12" cy="12" r="9.5" />
          <line x1="2.5" y1="12" x2="21.5" y2="12" />
          <path d="M12 2.5a14 14 0 0 1 3.6 9.5A14 14 0 0 1 12 21.5a14 14 0 0 1-3.6-9.5A14 14 0 0 1 12 2.5z" />
        </svg>
      );
  }
}
