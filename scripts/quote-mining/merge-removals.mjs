// Merge the 13-agent off-topic/controversial review into one durable artifact.
//
// Inputs (one-time, produced by the review agents into a scratch dir):
//   <REVIEW_DIR>/batch-0.json … batch-12.json   — the reviewed bank, 390 recs
//        each: { ref:"B<n>-<i>", key:<norm-lower quote>, category, author,
//                work, quote, expand }
//   <REVIEW_DIR>/remove-B0.json … remove-B12.json
//        each: { batch, reviewed, removals:[{ ref, rule, reason }] }
//
// Output (durable, committed-able, consumed by finalize-bank.mjs):
//   scripts/quote-mining/removed-quotes.json  — [{ key, category, rule,
//        reason, quote, author, work }]  (key = the finalize dedup key)
//   scripts/quote-mining/QUOTE-REMOVALS.md    — human-readable report
//
// The filter is keyed on the normalized lowercased quote text — identical to
// finalize-bank.mjs's dedup key — so finalize drops every matching quote from
// both the full bank and the re-sampled hero pool. Reproducible + reversible:
// delete removed-quotes.json (or its keys) and re-run finalize to restore.
//
//   REVIEW_DIR=/path/to/review node scripts/quote-mining/merge-removals.mjs
import fs from "node:fs";
import path from "node:path";

const QM = "C:\\Users\\losey\\Projects\\mycurricula.app\\scripts\\quote-mining";
const REVIEW =
  process.env.REVIEW_DIR ||
  path.join(process.env.CLAUDE_JOB_DIR || "", "tmp", "review");

if (!fs.existsSync(path.join(REVIEW, "batch-0.json"))) {
  console.error(`No batches found in REVIEW_DIR=${REVIEW}`);
  process.exit(1);
}

// ref -> record (across all 13 batches)
const byRef = {};
let batchTotal = 0;
for (let i = 0; i < 13; i++) {
  const arr = JSON.parse(fs.readFileSync(path.join(REVIEW, `batch-${i}.json`), "utf8"));
  batchTotal += arr.length;
  for (const r of arr) byRef[r.ref] = r;
}

const removed = [];
const seenKey = new Set();
let missing = 0;
let dupKey = 0;
let reviewedTotal = 0;
for (let i = 0; i < 13; i++) {
  const f = path.join(REVIEW, `remove-B${i}.json`);
  if (!fs.existsSync(f)) {
    console.error(`! missing ${f}`);
    continue;
  }
  const d = JSON.parse(fs.readFileSync(f, "utf8"));
  reviewedTotal += d.reviewed || 0;
  for (const rm of d.removals || []) {
    const rec = byRef[rm.ref];
    if (!rec) {
      missing++;
      continue;
    }
    if (seenKey.has(rec.key)) {
      // Same quote text flagged in two batches (a pre-dedup duplicate) — keep
      // one removal entry; the key drops every copy anyway.
      dupKey++;
      continue;
    }
    seenKey.add(rec.key);
    removed.push({
      key: rec.key,
      category: rec.category,
      rule: rm.rule || "other",
      reason: rm.reason || "",
      quote: rec.quote,
      author: rec.author || null,
      work: rec.work || null,
    });
  }
}

fs.writeFileSync(path.join(QM, "removed-quotes.json"), JSON.stringify(removed, null, 2));

// ── Report ────────────────────────────────────────────────────────────────
const byRule = {};
const byCat = {};
for (const r of removed) {
  byRule[r.rule] = (byRule[r.rule] || 0) + 1;
  byCat[r.category] = (byCat[r.category] || 0) + 1;
}
const ruleRows = Object.entries(byRule).sort((a, b) => b[1] - a[1]);
const catRows = Object.entries(byCat).sort((a, b) => b[1] - a[1]);

const esc = (s) => String(s || "").replace(/\|/g, "\\|").replace(/\n/g, " ");
let md = "# Quote Bank — Off-topic / Controversial Removals\n\n";
md += `_Generated by \`scripts/quote-mining/merge-removals.mjs\` from the 13-agent semantic review._\n\n`;
md += `Calibration (per the user): keep only quotes **directly** about teaching, learning, education-and-team leading, or classroom management. **Leading** = education & team leadership only (corporate/CEO leadership cut). **Parenting** kept only where it maps to the classroom. **EdTech/AI** kept only for concrete pedagogy (tech-politics & futurism cut). Law, religion, partisan politics, and otherwise controversial/off-topic content cut regardless of category.\n\n`;
md += `## Totals\n\n`;
md += `- **${removed.length}** unique quotes removed (of ${batchTotal} reviewed across 13 batches).\n`;
md += `- ${dupKey} duplicate-key flags collapsed; ${missing} removal refs had no matching record (skipped).\n\n`;
md += `## By rule\n\n| Removed | Rule |\n|---:|:--|\n`;
for (const [rule, c] of ruleRows) md += `| ${c} | ${esc(rule)} |\n`;
md += `\n## By category (of origin)\n\n| Removed | Category |\n|---:|:--|\n`;
for (const [cat, c] of catRows) md += `| ${c} | ${esc(cat)} |\n`;
md += `\n## Samples per rule (up to 4 each)\n\n`;
for (const [rule] of ruleRows) {
  md += `### ${rule}\n\n`;
  const ex = removed.filter((r) => r.rule === rule).slice(0, 4);
  for (const r of ex) {
    const by = [r.author, r.work].filter(Boolean).join(", ");
    md += `- _"${esc(r.quote.slice(0, 180))}${r.quote.length > 180 ? "…" : ""}"_${by ? ` — ${esc(by)}` : ""}  \n  → ${esc(r.reason)}\n`;
  }
  md += `\n`;
}
// Supplemental pass — hero-pool residuals re-admitted by re-derivation, plus
// off-topic source blocks (build-removed-extra.mjs → removed-extra.json).
let extra = [];
try {
  extra = JSON.parse(fs.readFileSync(path.join(QM, "removed-extra.json"), "utf8"));
} catch {
  /* none */
}
if (Array.isArray(extra) && extra.length) {
  const exRule = {};
  for (const r of extra) exRule[r.rule] = (exRule[r.rule] || 0) + 1;
  md += `## Supplemental pass — hero-pool residuals\n\n`;
  md += `A further **${extra.length}** quotes were removed after the main pass: residuals the re-derived bank re-admitted (finalize re-derives from source), plus four off-topic **source blocks** (Evolving Education · AI Learn Insights · AI Edu Simplified · Bauschard — AI/futurism/policy works that aren't timeless teaching wisdom). See \`removed-extra.json\`.\n\n`;
  md += `| Removed | Rule |\n|---:|:--|\n`;
  for (const [r, c] of Object.entries(exRule).sort((a, b) => b[1] - a[1])) md += `| ${c} | ${esc(r)} |\n`;
  md += `\n**Grand total removed: ${removed.length + extra.length}.**\n`;
}
fs.writeFileSync(path.join(QM, "QUOTE-REMOVALS.md"), md);

console.log(`Removed ${removed.length} unique quotes (${dupKey} dup-key, ${missing} missing refs).`);
console.log("By rule:", ruleRows.map(([r, c]) => `${r}=${c}`).join("  "));
console.log("By category:", catRows.map(([r, c]) => `${r}=${c}`).join("  "));
console.log(`reviewed≈${reviewedTotal}; batchTotal=${batchTotal}`);
